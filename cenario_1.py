    def desenhar_cenario(self): # Funcao para ler a matriz e desenhar o cenario

        indx = 0 # Recebera o valor da posicao da coluna para calcular o valor em pixel
        indy = 0 # Recebera o valor da posicao da linha para calcular o valor em pixel

        branco = 1.0
        cinza = 0.5

        cor = 0

        colunas = len(self.matriz_cenario[0])
        linhas = len(self.matriz_cenario)
        
        for x in range(colunas):
            indx = x
                    
            for y in range(linhas):
                indy = y

                # Pinta o chao quadriculado levando em conta a posicao na matriz
                if int(indx) % 2 == 0: # Identifica se a posicao da coluna e par ou impar
                    if int(indy) % 2 == 0: # Identifica se a posicao da linha e par ou impar
                        cor = branco
                    else:
                        cor = cinza
                else:
                    if int(indy) % 2 == 0: # Identifica se a posicao da linha e par ou impar
                        cor = cinza
                    else:
                        cor = branco

                # Desenha o chao onde for '0' na matriz
                if self.matriz_cenario[y][x] == 0:
                    glPushMatrix()

                    glBegin(GL_POLYGON)
                    glColor3f (cor, cor, cor)
                    glVertex3f (10*x + (-5), 10*y + (-5), 0.0)
                    glColor3f (cor, cor, cor)
                    glVertex3f (10*x + (-5), 10*y + 5, 0.0)
                    glColor3f (cor, cor, cor)
                    glVertex3f (10*x + 5, 10*y + 5, 0.0)
                    glColor3f (cor, cor, cor)
                    glVertex3f (10*x +5, 10*y + (-5), 0.0)
                    glEnd()
            
                    glPopMatrix()

                # Desenha a parede onde for '1' na matriz
                elif self.matriz_cenario[y][x] == 1:
                    self.desenharParede(10*x + (-5), 10*y + (-5),10*x + (-5), 10*y + 5,[150,0,0])
                    self.desenharParede(10*x + (-5), 10*y + 5,10*x + 5, 10*y + 5,[0,150,0])
                    self.desenharParede(10*x + 5, 10*y + 5, 10*x + 5, 10*y + (-5),[0,0,150])
                    self.desenharParede(10*x + 5, 10*y + (-5), 10*x + (-5), 10*y + (-5),[150,150,0])

